-- Markdown-specific indentation settings
-- Use 4 spaces for indentation in markdown files

vim.opt_local.shiftwidth = 4    -- Number of spaces for each indent level
vim.opt_local.tabstop = 4       -- Number of spaces a tab counts for
vim.opt_local.softtabstop = 4   -- Number of spaces for tab in insert mode
vim.opt_local.expandtab = true  -- Use spaces instead of tabs
